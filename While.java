public class While {
    public static void main(String[] args) {
    	int n = 1;
    	while ( n <= 100) {
    		if (n % 3 == 0) {
    		System.out.println("n=" + n);
    	}  n++;
    }
    	int j = 40;
    	while( j <= 200) {
    		if (j % 2 == 0) {
    		System.out.println("j=" + j);
    		}
    		j++;
    	}
    	
    	
    }
    
    }